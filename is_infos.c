#include "printf.h"

int		is_flag(char c)
{
	if ((c == '-') || (c == '+') || (c == ' ')
			|| (c == '#'))
		return (1);
	return (0);
}

int		is_modifier(char c)
{
	if ((c == 'l') || (c == 'j') || (c == 'z')
			|| (c == 'h'))				/* a completer ...[hh, ll]*/
		return (1);
	return (0);
}
